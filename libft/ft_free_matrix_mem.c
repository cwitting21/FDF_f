#include "libft.h"

char	**ft_free_matrix_mem(char ***s)
{
	char	*tmp;

	while (**s)
	{
		tmp = **s;
		free((**s)++);
		tmp = 0;
	}
	free(**s);
	**s = 0;
	free(*s);
	*s = 0;
	return (*s);
}
